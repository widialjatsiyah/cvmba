    public function pengajuanPembelianDetail($id)
    {

        $business_id = request()->session()->get('user.business_id');

        $taxes = TaxRate::where('business_id', $business_id)
            ->pluck('name', 'id');
        $purchase = Transaction::where('business_id', $business_id)
            ->where('id', $id)
            ->with(
                'contact',
                'purchase_lines',
                'purchase_lines.product',
                'purchase_lines.product.unit',
                'purchase_lines.product.second_unit',
                'purchase_lines.variations',
                'purchase_lines.variations.product_variation',
                'purchase_lines.sub_unit',
                'location',
                'payment_lines',
                'tax'
            )
            ->firstOrFail();

        if ($purchase->status == "ordered") {
            $status_pengajuan = '<span class="badge bg-info">Menuggu Persetujuan</span>';
        } else if ($purchase->status == "accepted") {
            $status_pengajuan = '<span class="badge bg-success">Disetujui</span>';
        } else if ($purchase->status == "decline") {
            $status_pengajuan = '<span class="badge bg-red">Ditolak</span>';
        } else if ($purchase->status == "completed") {
            $status_pengajuan = '<span class="badge bg-success">Selesai</span>';
        }else {
            $status_pengajuan = '<span class="badge badge-secondy">Tidak Ditemukan</span>';
        }


        foreach ($purchase->purchase_lines as $key => $value) {
            if (! empty($value->sub_unit_id)) {
                $formated_purchase_line = $this->productUtil->changePurchaseLineUnit($value, $business_id);
                $purchase->purchase_lines[$key] = $formated_purchase_line;
            }
        }

        $purchase_taxes = [];
        if (! empty($purchase->tax)) {
            if ($purchase->tax->is_tax_group) {
                $purchase_taxes = $this->transactionUtil->sumGroupTaxDetails($this->transactionUtil->groupTaxDetails($purchase->tax, $purchase->tax_amount));
            } else {
                $purchase_taxes[$purchase->tax->name] = $purchase->tax_amount;
            }
        }

        //Purchase orders
        $purchase_order_nos = '';
        $purchase_order_dates = '';
        if (! empty($purchase->purchase_order_ids)) {
            $purchase_orders = Transaction::find($purchase->purchase_order_ids);


            $purchase_order_nos = implode(', ', $purchase_orders->pluck('ref_no')->toArray());
            $order_dates = [];
            foreach ($purchase_orders as $purchase_order) {
                $order_dates[] = $this->transactionUtil->format_date($purchase_order->transaction_date, true);
            }
            $purchase_order_dates = implode(', ', $order_dates);
        }

        return view('home.mobile.pengajuan.pengajuanPembelianDetail')->with(compact('taxes', 'purchase',  'purchase_taxes', 'purchase_order_nos', 'purchase_order_dates', 'status_pengajuan'));
    }

    public function updatePengajuanAcc($id)
    {
        try {
            // Memulai transaksi
            DB::beginTransaction();

            // Cari data transaksi yang ingin diperbarui
            $transaction = Transaction::findOrFail($id); // Ganti $id dengan ID transaksi

            // Perbarui data transaksi
            $transaction->update([
                'status' => "accepted",     // Contoh kolom lain yang diperbarui
            ]);

            // Commit transaksi jika tidak ada error
            DB::commit();

            return response()->json([
                'success' => true,
                'message' => 'Transaction updated successfully!'
            ]);
        } catch (\Exception $e) {
            // Rollback jika terjadi error
            DB::rollBack();

            // Tangani error, misalnya kirim respons error
            return response()->json(['message' => $e->getMessage()], 500);
        }
    }

